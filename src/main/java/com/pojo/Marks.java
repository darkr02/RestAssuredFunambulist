package com.pojo;

public class Marks {
    private int bengali;
    private int english;

    public Marks(int bengali, int english) {
        this.bengali = bengali;
        this.english = english;
    }

    public int getBengali() {
        return bengali;
    }

    public int getEnglish() {
        return english;
    }
}
